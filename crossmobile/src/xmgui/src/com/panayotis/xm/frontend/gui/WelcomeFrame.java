    private void CreateBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBActionPerformed
        chooser.setDialogTitle("Create Project");
        chooser.setDialogType(JFileChooser.SAVE_DIALOG);
        if (chooser.showDialog(this, "Create") == JFileChooser.APPROVE_OPTION) {
            Project p = new Project(chooser.getSelectedFile().getPath());
            if (p.isWritable()) {
                new ProjectFrame(p).setVisible(true);
                setVisible(false);
                dispose();
            } else
                JOptionPane.showMessageDialog(this, "No CrossMobile project can be created under " + p.getPath(), "Error while creating project", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_CreateBActionPerformed

