    private void OpenBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenBActionPerformed
        chooser.setDialogTitle("Open Project");
        chooser.setDialogType(JFileChooser.OPEN_DIALOG);
        if (chooser.showDialog(this, "Open") == JFileChooser.APPROVE_OPTION) {
            Project p = new Project(chooser.getSelectedFile().getPath());
            if (p.exists()) {
                new ProjectFrame(p).setVisible(true);
                setVisible(false);
                dispose();
            } else
                JOptionPane.showMessageDialog(this, "No CrossMobile project exists under " + p.getPath(), "Error while opening project", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_OpenBActionPerformed
