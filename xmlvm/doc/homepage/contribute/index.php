<h3>Getting and compiling the source</h3>
Our source code is currently hosted in our Subversion repository at <a href="http://www.sourceforge.net/projects/xmlvm">Sourceforge</a>. In order to check out the code, you need a <a href="http://subversion.tigris.org/">Subversion</a> client. As the team works with <a href="http://www.eclipse.org">Eclipse</a>, our recommendation is to use <a href="http://subclipse.tigris.org/">Subclipse</a>, which is a free Eclipse plugin that allows you to easily checkout and update your code. We would recommend you to install this plugin as well, if you are planning to work with Eclipse.

The root for checking out the code is:

<pre>http://xmlvm.svn.sourceforge.net/svnroot/xmlvm/trunk/xmlvm</pre>

If you want to checkout the code via the Subversion <pre>svn</pre> cient, use the following command:

<pre>svn co http://xmlvm.svn.sourceforge.net/svnroot/xmlvm/trunk/xmlvm xmlvm</pre>

